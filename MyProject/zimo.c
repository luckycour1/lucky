// 画字
void Draw_ZI(int x, int y, unsigned char zi[], int color, int length){
    for(int i = 0; i < length; i++){
        for(int j = 7; j >= 0; j--) //遍历16进制码的每一个bit位
	    {
	        if(zi[i]>>j & 1) //判断该bit位是否要上色
	        {
	            //x+(7-i)将16进制码 与其对应的像素点的横坐标确定下来
	            lcd_DrawPixel(x + 8*(i%3) + (7-j), y + i/3, color); //将对应的像素点显示一个相应的颜色
	        }
	    }
    }
}

